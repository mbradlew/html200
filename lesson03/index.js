// Declare four variables
let a = 10;
let b = 5;
let c = 20;
let d = 2;

// Addition
let sum = a + b;
console.log('Sum: ' + sum); // Output: Sum: 15

// Subtraction
let difference = c - a;
console.log('Difference: ' + difference); // Output: Difference: 10

// Multiplication
let product = b * d;
console.log('Product: ' + product); // Output: Product: 10

// Division
let quotient = c / d;
console.log('Quotient: ' + quotient); // Output: Quotient: 10

// Modulus
let remainder = c % b;
console.log('Remainder: ' + remainder); // Output: Remainder: 0

// Exponentiation
let power = a ** d;
console.log('Power: ' + power); // Output: Power: 100

// Combining operations
let combined = (a + b) * (c - d) / b;
console.log('Combined Operation Result: ' + combined); // Output: Combined Operation Result: 20
